/**
 * @brief Stores data records held in the manifest JSON file.
 *
 * @internal @note \c char \b not \c wchar_t is critical here.
 *
 * @ingroup POD
 */
struct JSONData
{
  /// Canonical path to the image file as a UTF-8 string.
  std::string file_path;
  /// Canonical path to the image thumbnail file as a UTF-8 string.
  std::string cache_file_path;
  /// Width of the image in pixels.
  int width;
  /// Height of the image in pixels.
  int height;
};


